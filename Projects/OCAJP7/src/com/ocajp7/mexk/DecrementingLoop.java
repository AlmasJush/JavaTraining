package com.ocajp7.mexk;

public class DecrementingLoop {

	public static void main(String[] args) {
		
		
		for(int i=10; i>0; i--){
			System.out.print(i + ", ");
		}
		System.out.println();
		int j;
		for(j=0; j<=11; j++){
			System.out.print(j + " ");
		}
		
		System.out.println();
		System.out.println(j);
	}

}
